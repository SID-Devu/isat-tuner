def generate_script(hw: HardwareProfile, model_path: str) -> str:
    """Generate a complete, runnable inference script for the detected hardware."""
    model_mb = 0.0
    p = Path(model_path)
    if p.exists():
        model_mb = p.stat().st_size / (1024 * 1024)
        seen = {p.resolve()}
        for suffix in [".onnx_data", "_data"]:
            dp = p.parent / (p.stem + suffix)
            if dp.exists() and dp.resolve() not in seen:
                seen.add(dp.resolve())
                if dp.is_dir():
                    model_mb += sum(f.stat().st_size for f in dp.iterdir()) / (1024 * 1024)
                else:
                    model_mb += dp.stat().st_size / (1024 * 1024)
        for pat in ["*.onnx_data", "*.onnx.data", "*.data"]:
            for ed in p.parent.glob(pat):
                if ed.resolve() not in seen and ed.is_file():
                    seen.add(ed.resolve())
                    model_mb += ed.stat().st_size / (1024 * 1024)

    fp_hash = _fingerprint(hw, model_path)
    gpu = hw.primary_gpu

    if not gpu:
        return _cpu_script(hw, model_path, model_mb, fp_hash)

    is_windows = hw.os_name == "windows"

    # Windows: prefer MIGraphX via WinML CompileApi if AMD EP AppX is installed
    if is_windows and hw.winml_ep_path and gpu.vendor == "amd":
        return _winml_migraphx_script(hw, gpu, model_path, model_mb, fp_hash)
    if is_windows:
        return _directml_script(hw, gpu, model_path, model_mb, fp_hash)

    if gpu.vendor == "amd":
        if gpu.gpu_type == "apu":
            return _amd_apu_script(hw, gpu, model_path, model_mb, fp_hash)
        return _amd_dgpu_script(hw, gpu, model_path, model_mb, fp_hash)
    elif gpu.vendor == "nvidia":
        return _nvidia_script(hw, gpu, model_path, model_mb, fp_hash)
    elif gpu.vendor == "intel":
        return _intel_script(hw, gpu, model_path, model_mb, fp_hash)
    elif gpu.vendor == "apple":
        return _apple_script(hw, gpu, model_path, model_mb, fp_hash)
    elif gpu.vendor == "qualcomm":
        return _qualcomm_script(hw, gpu, model_path, model_mb, fp_hash)
    else:
        return _cpu_script(hw, model_path, model_mb, fp_hash)


def save_script(hw: HardwareProfile, model_path: str,
                output_dir: str = "isat_output") -> str:
    """Generate and save the script to a file. Returns the file path."""
    script = generate_script(hw, model_path)
    model_name = _model_basename(model_path)
    out_dir = Path(output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)
    out_path = out_dir / f"{model_name}_inference.py"
    out_path.write_text(script)
    out_path.chmod(0o755)
    return str(out_path)

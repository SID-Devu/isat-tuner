def _get_swap_gb() -> float:
    try:
        for line in Path("/proc/meminfo").read_text().splitlines():
            if line.startswith("SwapTotal:"):
                return int(line.split()[1]) / (1024 * 1024)
    except OSError:
        pass
    return 0.0


def _get_vram_mb() -> int:
    for card in sorted(Path("/sys/class/drm").glob("card*/device")):
        vt = card / "mem_info_vram_total"
        if vt.exists():
            try:
                return int(vt.read_text().strip()) // (1024 * 1024)
            except (OSError, ValueError):
                pass
    return 0


def _get_gtt_mb() -> int:
    for card in sorted(Path("/sys/class/drm").glob("card*/device")):
        gt = card / "mem_info_gtt_total"
        if gt.exists():
            try:
                return int(gt.read_text().strip()) // (1024 * 1024)
            except (OSError, ValueError):
                pass
    return 0



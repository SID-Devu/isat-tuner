def _fingerprint(hw: HardwareProfile, model_path: str) -> str:
    """Generate a unique hash for this hardware + model combination."""
    gpu = hw.primary_gpu
    identity = (
        f"{hw.cpu_name}:{hw.system_ram_mb}:"
        f"{gpu.vendor if gpu else 'cpu'}:{gpu.arch if gpu else ''}:"
        f"{gpu.vram_mb if gpu else 0}:{gpu.shared_mem_mb if gpu else 0}:"
        f"{model_path}"
    )
    return hashlib.sha256(identity.encode()).hexdigest()[:12]


def _model_basename(model_path: str) -> str:
    return Path(model_path).stem.replace("-", "_").replace(".", "_")



def create_session():
    """Create an ORT inference session with MIGraphX EP."""
    import onnxruntime as ort

    sess_opts = ort.SessionOptions()
    sess_opts.graph_optimization_level = ort.GraphOptimizationLevel.ORT_DISABLE_ALL

    providers = [
        ("MIGraphXExecutionProvider", {{
            "migraphx_fp16_enable": "1",
            "device_id": "0",
        }}),
        "CPUExecutionProvider",
    ]

    print("Loading model and compiling with MIGraphX...")
    print("(First run compiles the model — this can take minutes for large models)")
    print()

    t0 = time.perf_counter()
    session = ort.InferenceSession(MODEL_PATH, sess_opts, providers=providers)
    compile_time = time.perf_counter() - t0

    # CRITICAL: Verify MIGraphX is actually active
    active_ep = session.get_providers()[0]
    if "MIGraphX" not in active_ep:
        print(f"  [WARNING] Expected MIGraphX but got: {{active_ep}}")
        print(f"  ORT silently fell back to CPU. Check your ORT build.")
    else:
        print(f"  [OK] Running on: {{active_ep}}")

    print(f"  Compile time: {{compile_time:.1f}}s")
    print()
    return session


